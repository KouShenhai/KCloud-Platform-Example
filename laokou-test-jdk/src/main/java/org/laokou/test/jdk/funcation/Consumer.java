package org.laokou.test.jdk.funcation;

import java.util.Objects;

/**
 * @author laokou
 */
@FunctionalInterface
public interface Consumer<T>{

    /**
     * 唯一抽象方法
     * @param t
     */
    void accept(T t);

    /**
     * 可以有多个非抽象方法（非默认）
     * @param after
     * @return
     */
    default Consumer<T> andThen(Consumer<? super T> after) {
        Objects.requireNonNull(after);
        return (T t) -> {
            accept(t);
            after.accept(t);
        };
    }

}
